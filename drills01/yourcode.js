/* 
Given an array of numbers, add up all the numbers
input: [1,5,3,2,6]
output: returns 17
*/

function sumArray(inputArray){
    var sum =0;
    for(indexArray = 0; indexArray<inputArray.length;indexArray++){
        sum += inputArray[indexArray];
    }
    return sum;
}


